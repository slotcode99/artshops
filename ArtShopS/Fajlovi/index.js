// Funkcija za otvaranje modalnog prozora
function otvoriModalSestiDva() {
    modalniProzorSestiDva.style.display = "block";
}

// Funkcija za zatvaranje modalnog prozora
function zatvoriModalSestiDva() {
    modalniProzorSestiDva.style.display = "none";
}

// Zatvori modalni prozor klikom van njega
window.addEventListener("click", function(event) {
    if (event.target === modalniProzorSestiDva) {
        zatvoriModalSestiDva();
    }
});

// Zatvori modalni prozor prilikom učitavanja stranice
window.addEventListener("load", function() {
    zatvoriModalSestiDva();
});



